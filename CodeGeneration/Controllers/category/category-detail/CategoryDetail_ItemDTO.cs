
using WG.Entities;
using Common;
using System;
using System.Linq;
using System.Collections.Generic;

namespace WG.Controllers.category.category_detail
{
    public class CategoryDetail_ItemDTO : DataDTO
    {
        
        public long Id { get; set; }
        public string Code { get; set; }
        public string Name { get; set; }
        public string SKU { get; set; }
        public string Description { get; set; }
        public long TypeId { get; set; }
        public long StatusId { get; set; }
        public long PartnerId { get; set; }
        public long CategoryId { get; set; }
        public long BrandId { get; set; }
        public CategoryDetail_BrandDTO Brand { get; set; }
        public CategoryDetail_PartnerDTO Partner { get; set; }
        public CategoryDetail_ItemStatusDTO Status { get; set; }
        public CategoryDetail_ItemTypeDTO Type { get; set; }
        public CategoryDetail_ItemDTO() {}
        public CategoryDetail_ItemDTO(Item Item)
        {
            
            this.Id = Item.Id;
            this.Code = Item.Code;
            this.Name = Item.Name;
            this.SKU = Item.SKU;
            this.Description = Item.Description;
            this.TypeId = Item.TypeId;
            this.StatusId = Item.StatusId;
            this.PartnerId = Item.PartnerId;
            this.CategoryId = Item.CategoryId;
            this.BrandId = Item.BrandId;
            this.Brand = new CategoryDetail_BrandDTO(Item.Brand);

            this.Partner = new CategoryDetail_PartnerDTO(Item.Partner);

            this.Status = new CategoryDetail_ItemStatusDTO(Item.Status);

            this.Type = new CategoryDetail_ItemTypeDTO(Item.Type);

        }
    }

    public class CategoryDetail_ItemFilterDTO : FilterDTO
    {
        
        public long? Id { get; set; }
        public string Code { get; set; }
        public string Name { get; set; }
        public string SKU { get; set; }
        public string Description { get; set; }
        public long? TypeId { get; set; }
        public long? StatusId { get; set; }
        public long? PartnerId { get; set; }
        public long? CategoryId { get; set; }
        public long? BrandId { get; set; }
    }
}
